							<p>3. A gastronomia</p>
							<p> A gastronomia de Malta tem uma influência italiana, então, acabei comendo muiiiiitaa pizza e outros tipos de massas por lá. Em todo canto tem pizza para comprar, e elas são boas e baratas!</p>
							<p> Recomendo o Gululu Restaurant, que tem a localização perfeita, onde você pode admirar a Spinola Bay e comer pratos típicos de Malta, como o rabbit (carne de coelho), que experimentei com meus amigos e acredite se quiser: é bom! </p>
							<p>O restaurante do Café Del Mar, que fica em St Paul’s Bay, também é incrível. Jantar vendo o pôr do sol é maravilhoso! O cardápio é bem variado, se for com os amigos, eles tem opções “for share” para petiscar, e o vinho branco maltês é indescritível! </p>
							<p>Além disso, você não pode ir embora de Malta sem experimentar o Pastizzi (um folhado recheado), tem em todas as esquinas para vender e custa apenas alguns cent €.</p>

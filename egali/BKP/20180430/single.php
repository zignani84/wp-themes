							<p>4. Compras </p>
							<p>Fiz muiiitas compras de blusinhas, vestidos e shorts, super verão! Também aproveitei para comprar casacos, pois os preços estavam muito acessíveis!</p>
							<p>Indico o The Point Shopping Mall que fica em Sliema, é rápido de chegar pegando um ônibus em St.Julian’s. Ele é bem grande, tem muitas lojas e muitas promoções, é só procurar que você acha. Vale a pena!</p>

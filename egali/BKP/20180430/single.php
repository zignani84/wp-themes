							<p>5. Os passeios</p>
							<p> Na ilha de Malta, recomendo o passeio para St. Peter’s Pool. É simplesmente lindo! A formação rochosa é singular, ótimo para quem curte dar uns pulos cheios de adrenalina e também para quem quer ficar “de boa” apreciando o lugar.</p>
							<p> Na ilha de Comino, o lugar mais maravilhoso foi a Blue Lagoon, a água é realmente cristalina, é uma delícia nadar, e os tons de azul são de encantar, É o paraíso! Inclusive, foi lá, com a vista de toda a Blue Lagoon, que meu namorado me pediu em noivado, uma surpresa apaixonante em um lugar inesquecível!</p>

							<p>7. A experiência</p>
							<p>Minha experiência com o intercâmbio em Malta foi ótima, com certeza, eu faria de novo! A cultura deles é muito bem conservada, principalmente em cidades como Valletta e Birgu, com construções em calcário, monumentos ricos em história e casinhas com portas e janelas coloridas. </p>
							<p>Foi difícil me adaptar com o verão! É muito quente em julho, e parece que a luz do sol dura 24 horas hahaha. Usei muito protetor solar, tomei muita água, botei um chapéu e óculos de sol e escolhia horários com a temperatura mais amena para visitar outras cidades. </p>
							<p>Vou contar uma verdade: o povo maltês às vezes não é muito simpático, mas as paisagens e pontos turísticos fazem valer muito a pena toda essa experiência.</p>
